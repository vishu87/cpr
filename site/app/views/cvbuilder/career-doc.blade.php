                    <h4>Structure</h4>
                    <ol>
                      <li>The CorperLIfe™ Cv builder should take care of the general structure and flow of the CV. Check out examples of cvs built using the cv builder. (Show different Cv created using CV builder)</li>
                      <li>Your CV should be between 1-2 pages. You want to look like you have enough to say and can say it efficiently.</li>
                      <li> Recruiters will focus more on the first page of your resume so make sure, the most recent, important and relevant information can be found there.  </li>
                    </ol>
                    <h4>Content</h4>
                    <ol>
                      <li>The most Important things employers look for in a Cv are:
                        <ul>
                          <li> Previous related work experience </li>
                          <li>Qualifications and Skills.</li>
                          <li>Education</li>
                          <li>Accomplishments</li>
                          <li>Community service</li>
                        </ul>
                      </li>
                      <li>The average cv gets read in  10 seconds so think it out and make every word count. </li>
                      <li>Put most recent experiences first.</li>
                      <li>Get rid of the “objective” or “mission statement”. That stuff is generic and recruiters don't waste their time with those.</li>
                      <li> Describe past career experiences in terms of achievements (use Bullet points to clearly define them).</li>
                      <li> Do not rely on opinionated statements. Use actual facts and figures in Cvs. </li>
                      <li>Read through the job posting properly and try to incorporate the same or similar words into your CV</li>
                      <li>Spell check and re-read your Cv (1,000 times) This is VERY important.</li>
                      <li>Get your CV proof-read by someone else, they will see flaws that you cannot.</li>
                      <li>Do not use abbreviations (e.g. they’re, don’t, can’t, wouldn’t, etc); instead, fully spell out words (e.g. they are, do not, can not, would not, etc.)  </li>
                      <li>Be Honest!! Do not misrepresent or tell “untrue statements”(i.e Lies). It is the recruiters job to test your story, and some will ask you about everything.
                          Make sure you are able to support any claims you make.</li>
                      <li>Create custom Cvs for each job you are applying for….. a different cv for every application. This is so you can tailor your Cv to exactly what each employer is
                          looking for. Easily Create/ alter Cvs using the CorperLife™  Cv Builder. Go on…... its free!</li>
                      <li> Use “Action words” like: executed, assisted, planned, developed, etc. when talking about past experiences.</li>
                      <li>Only include Hobbies and interest section if it is relevant to the potential employer</li>
                    </ol>
                    <h4>Format</h4>
                      <ol>
                        <li>Remember recruiters are human beings and your CV might be one of 100+ that they might go through in a day, so make sure yours is:- 
                          <ul>
                            <li>Interesting - Put most your interesting and relevant experiences on the CV. Make sure you are remembered and don't bore the recruiter. This is no time to be humble!</li>
                            <li>Short and straight to the point….. sometimes less is more.</li>
                            <li>Broken up into easy-to-read sections. The CorperLife™ cv builder (link to cv builder) is designed to assist you with that.</li>
                            <li>Properly formatted with use of Bullet Points</li>
                          </ul>
                        </li>
                        <li> Many cvs are read on screens, so make sure yours is computer screen friendly. </li>
                        <li>Use serious Text fonts. sans Fonts (not curvey) are preferable but never, ever….. ever use comic sans.</li>
                        <li>Font size should be around 12pts. It is big enough to read and small enough to make you look serious/ intelligent.</li>
                        <li>Save the CV as your name</li>
                        <li>We recommend saving your CV in PDF format so it cannot be tampered with.</li>
                      </ol> 
                    <h4>Delivery</h4>
                    <ol>
                      <li> When posting/ submitting your CV Don't fold it. Show yourself some respect and put it in an A4 sized envelope.</li>
                      <li>If you are emailing your cv to an employer put your cover letter as the body of the email and your cv as an attachment.</li>
                      <li>Make sure your contact informations is accurate so that it gets to the right person.</li>
                      <li>When you apply to a job advert be careful of the information you give. Be diligent and do your research  and make sure the company is not a fraud. </li>
                      <li>If your CV is not getting you the results you want, change it. Keep modifying it till employers start calling. Remember applying for jobs is a Job and should be treated as such.</li>
                      <li>Use a professional email address (firstname_lastname@blah blah.com) to send your resume to recruiters. If you don't have one ….its about that time to get it. </li>
                    </ol>
