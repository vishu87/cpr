                      <h4>Before Interview</h4> 
                      <ol>
                        <li>Do some research on the business before the interview. (very important!!) </li>
                        <li>Try to meet someone who already works there and build a relationship</li>
                        <li>Fill out an application (if ...er…...applicable)</li>
                        <li>Practice interviewing. (check out CorperLIfe™ Common interview questions)</li>
                        <li> Make your elevator pitch! (a short summary used to simply and quickly defined you)</li>
                        <li>If you can schedule the interview do it on a Tuesday 10:30….. its mid-week.. and everyone hates mondays.</li>
                        <li>Go alone. Do not take children or friends.</li>
                        <li> Bring your "Fact Sheet" with telephone numbers and addresses of your references and former employers, just in case you are asked to complete an 
                            application. </li>
                        <li>Look Clean and tidy and dress like the job you want to get (first impressions count)</li>
                        <li>(Advice) Do not wear red (it takes attention from you)</li>
                        <li>(Advice) Use subtle makeup</li>
                        <li>(Advice) No perfume</li>
                        <li>Prepare questions to ask interviewer</li>
                        <li>Clean up your Social Media accounts …. cheack out www.socialsweepster.com</li>
                        <li> Plan to Be there 30mins before time walk into building 10mins before time</li>
                        <li> Use restroom prior to interview  so you are comfortable.</li>
                      </ol>
                      <h4>During Interview</h4>
                      <ol>
                        <li>Greet the employer with a handshake.</li>
                        <li>Make frequent eye contact.</li>
                        <li> Smile and be polite</li>
                        <li>Stay Calm….. the interviewers have sat where you are before, they are just people.</li>
                        <li> Don’t be too chilled and let your guard down</li>
                        <li>Listen carefully to the questions asked. Ask the interviewer to restate a question if you are confused.</li>
                        <li> Answer questions as directly as possible. No meaningless long stories.</li>               
                        <li>Be upbeat and make positive statements.</li>
                        <li>Never focus on the potential salary, it should never be your motivation. Focus more on the opportunities.</li>
                        <li>If you've worked before, talk about what you learned from it.</li>
                        <li>Before your interview ends, ask this one last question: “Have I said anything in this interview or given you any other reason to doubt that I am a good 
                            fit for the role?”</li>
                        <li>Use examples of how your skills and abilities would fit the job.</li>
                      </ol>  
                      <h4>After Interview</h4>
                      <ol>
                        <li>Thank Interviewer for their time</li>
                        <li>Request a business card (to help with note)</li>
                        <li>Review your interview while your memory is fresh (write down points to improve on e.g  questions you could have answered better)</li>
                        <li>(2-3 days later) email or better yet , send Handwritten thank you note</li>
                      </ol>
