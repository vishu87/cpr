              <p>One word to describe this day... you guessed it, freedom. At the time this overview was written (Oct 2014), the elaborate ceremony had been suspended to ensure the safety of corpers during civil unrest. It usually involves a passing out parade and speeches from prominent people in the state. Sounds boring right? There is really only two reasons to go for it; your certificate and friends. You get to see everyone you knew in camp and for most of them this will probably be the last time. So make sure you exchange each other’s contacts and hang out after. By making proper friends and helping each other in life you will all become more successful.</p>
              <p>So now that you're an ex-corper and have explored more of Nigeria you have probably noticed that this country is filled with so many problems. The serving experience is meant to show you how much of a difference you can make in your youth. It is no longer enough to be a bystander and blame the government for all the problems. You are both people and the government, so it is up to you go out there and make things happen. It’s time to graduate from being a corper to becoming an agent of change, a hero. Go and take those problems head-on, better yet see them as opportunities and solve them, for this is how great businesses are born.</p>
              <p>Enough of the serious talk, we believe congratulations are in order!, if you took our advice and experienced the whole thing then some serious kudos to you, wear it as a badge of honour. You have more experience, have made more friends and possible future connections thus gaining advantage that most of your peers won’t have. For those who purposefully skipped the NYSC experience we simply pray for courage to embrace future opportunities/challenges.</p>
              <p>In all your excitement please don't forget to make new 'last minute' friends, secure older ones, take loads of pics and don't forget to use the CorperLife<sup>TM</sup> career centre. Now go forth and be great!!... thanks for using CorperLIfe<sup>TM</sup>.</p>
