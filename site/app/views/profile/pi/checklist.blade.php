              <p>
                 Camp begins with finding out where you will be sleeping and receiving your kit. Before we continue, it is important you understand a few things have changed recently, for one, foreign students now do their verification in camp rather than the tedious trip to Abuja also, you get your call up number in text or online. The government has clearly made an effort to simplify the process but it may take some time for those higher level changes to efficiently show at camp and local gov't level.</p>
                 <p>Since verification and settling-in will be happening at the same time, our prediction is that you will probably spend more than two days going through it all. If it proves to be otherwise, please let us know on our Facebook page and we will send the government an official Corperlife<sup>TM</sup> commendation (we are serious)</p>
                  <p>Don't.Stress.Yourself. Settling in will take a few days and camp won't start till everyone is settled in, so just go with the flow, there will DEFINITELY be enough time to get yourself sorted out. As far as living goes, the bedrooms will be packed, and the bathrooms can be below standard (degree varies by state), there are also lots of boring lectures to attend and you WILL be woken up at 4:30ish by the sound of the almighty bugle, whistles and shouting soldiers. That aside, camp is by far the most fun experience in NYSC and one worth remembering and sharing with friends. This is your chance to do it so don't cheat yourself out of an amazing experience by skipping it. Camp is a great opportunity to make new friends, relax and enjoy the freedom of being absolutely carefree for three weeks.</p>
                  <p>We haven't experienced other camps but here is what you can expect if you are heading to Iyana Ipaja (Lagos) or any similarly cool camp.</p>
                 <p>It is essentially a government sponsored retreat. The highlight is Mami market where you will (should) spend most of your time. Everyone that has been through camp knows anything is possible in Mami; friends, future spouses, groceries, laundry services, bukkas with correct food, tailors, barbers/hairdressers and even a video game arcade! At night, the Mami market basically transforms into a nightclub/bar with current music, drinks and all kinds of food (like shawarma, suya, small chops, even cotton-candy). And you know what the best part is? Everything is offered at corper rates...i.e dirt cheap.</p>
                 <p>You will also experience some company sponsored competitions with the possibility of earning nice prizes, and if you're lucky, your camp may even have a concert performed by a surprise big-name artist. During all this, many inter-platoon competitions, reminiscent of secondary school inter-house sports, will be going on. The officials do a good job of making sure there are always things to do.</p>
                 <p>Regardless which camp you went to, at the end of the 3 weeks you won't be able to help but feel a bit sad about leaving. It's like that last day in boarding school, you will miss the once hostile environment and all the people you are leaving. Hopefully you diligently collected contact details of all your camp friends, so keeping in touch won't be an issue. All in all, camp is a once-in-a-lifetime experience you really don't want to miss out on. So set your expectations low, make friends, learn, take lots of pictures and keep up with Corperlife<sup>TM</sup>.</p>
              
