              <p>
                 If there is any part of the NYSC that teaches you humility and how to "kiss ass", it's the serving phase better known as the PPA (Place of Primary Assignment). Your PPA is where you will be posted for the rest of your service year. It could be anywhere from a school to your local government office. It all depends on what you got last day of camp. The Serving phase usually involves a couple of things:-
              </p>
              <ol>
                <li>CD/SAED Groups: - Pick one; either develop the community (CD) or develop yourself (SAED), you do these weekly. Don't forget to get your respective cards signed.</li>
                <li>Monthly General CDS: - Every corper in the Local Government gathers together on this day to listen to the LGI’s words of wisdom and important announcements. We would recommend coming to listen, in fact, go as far as giving the occasional nod of agreement because your dear LGI signs your card after.</li>
                <li>Monthly Clearance: - Done in the first week of the month, you get a letter from your employer saying that you worked well for the past month and you deserve to get paid. Without this you don't get allowee so don't dull.</li>
                <li>Final Clearance: - As the name says, this is the last time you do clearance. So make sure you don't vex anyone before this day. They can really make life hard for you</li>
              </ol>
              <p>"Exactly whose ass will I be kissing?" Did we hear you ask…. the answer is simple: anyone who has to sign anything for you, especially your boss and your LGI, they have true power. It is very advisable to get into and stay in their good books. Try not to become "teacher's pet" though as you might run the risk of being hated by fellow corpers. So Kiss-ass, make friends, take loads of pics and build your NYSC story with CorperLIfe<sup>TM</sup>!</p>
